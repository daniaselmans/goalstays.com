// Helper function to get hotels by city
export const getHotelsByCity = (city: string): Hotel[] => {
  return mockHotels.filter(hotel => 
    hotel.city.toLowerCase().includes(city.toLowerCase()) ||
    hotel.location.toLowerCase().includes(city.toLowerCase())
  );
};

// Helper function to get featured hotels
export const getFeaturedHotels = (): Hotel[] => {
  return mockHotels.filter(hotel => hotel.featured);
};

// Helper function to get hotels with discounts
export const getDealsHotels = (): Hotel[] => {
  return mockHotels.filter(hotel => hotel.discount && hotel.discount > 0);
};
